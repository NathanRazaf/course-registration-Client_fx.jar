package client_fx;

/**
 * La classe RunMain a été créée pour pouvoir lancer l'application à partir de la ligne de commande.
 */
public class RunMain {
    /**
     * La méthode main exécute la méthode main de la classe Main.
     * main prend comme paramètre args, une liste de String, qui représente un argument de la ligne de commande.
     *
     * @param args arguments de la ligne de commande
     */
    public static void main(String[] args) {
        Main.main(args);
    }
}
